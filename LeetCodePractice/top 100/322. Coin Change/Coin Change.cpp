class Solution {
public:
	int coinChange(vector<int>& coins, int amount) {
		int n = coins.size();
		if (n <= 0)
			return 0;
		vector<int> dp(amount + 1, amount + 1);//注意这里初始化的值，如果用INT_MAX会出错
		dp[0] = 0;
		for (int i = 1; i <= amount; ++i)
		{
			for (int j = 0; j < coins.size(); ++j)
			{
				if (coins[j] <= i)
					dp[i] = min(dp[i], dp[i - coins[j]] + 1);
			}
		}
		return dp[amount] >= amount + 1 ? -1 : dp[amount];//注意这里
	}
};