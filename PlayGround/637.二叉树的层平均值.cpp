class Solution
        {
        public:
            vector<double> averageOfLevels(TreeNode* root)
            {
                vector<double> res;
                queue<TreeNode*> q;
                q.push(root);
                while (!q.empty())
                {
                    int size = q.size();
                    long long total = 0;
                        for (int i = 0; i < size; ++i)
                        {
                            auto nod = q.front();
                            q.pop();
                            total += nod->val;
                            if (nod->left)
                                q.push(nod->left);
                            if (nod->right)
                                q.push(nod->right);
                        }
                        res.push_back((double)total / size);
                }
                return res;
            }
        };
